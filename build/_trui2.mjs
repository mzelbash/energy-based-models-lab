import fs from 'fs'; import puppeteer from 'puppeteer-core';
const C=['C:/Program Files/Google/Chrome/Application/chrome.exe','C:/Program Files (x86)/Microsoft/Edge/Application/msedge.exe'].find(p=>fs.existsSync(p));
const wait=ms=>new Promise(r=>setTimeout(r,ms));
const b=await puppeteer.launch({executablePath:C,headless:'new',args:['--no-sandbox','--use-gl=swiftshader']});
const p=await b.newPage(); const errs=[]; p.on('pageerror',e=>errs.push(e.message)); p.on('console',m=>{if(m.type()==='error'&&!m.text().includes('favicon'))errs.push(m.text());});
await p.goto('http://localhost:8000/',{waitUntil:'networkidle2',timeout:60000});
await p.waitForFunction(()=>{const t=document.querySelector('#model-status .txt');return t&&t.textContent.includes('loaded');},{timeout:45000}).catch(()=>{});
await p.$$eval('.tab-btn',e=>e[5].click()); await wait(400);
const hasBufInfo=await p.evaluate(()=>[...document.querySelectorAll('#tab-training .callout__label')].some(e=>e.textContent.includes('replay buffer for')));
await p.evaluate(()=>{const s=document.getElementById('tr-steps');s.value='40';s.dispatchEvent(new Event('input'));});
await p.click('#tr-run');
await wait(400);
const t0=await p.evaluate(()=>({visible:!document.getElementById('tr-progress').hidden, label:document.getElementById('tr-progress-label').textContent, btn:document.getElementById('tr-run').textContent}));
await wait(12000);
const t1=await p.evaluate(()=>({width:document.getElementById('tr-progress-fill').style.width, label:document.getElementById('tr-progress-label').textContent}));
await p.click('#tr-stop');
await wait(4000);
const t2=await p.evaluate(()=>({label:document.getElementById('tr-progress-label').textContent, btn:document.getElementById('tr-run').textContent, status:document.querySelector('#tr-status .txt').textContent}));
console.log('bufferInfo:',hasBufInfo);
console.log('atStart:',JSON.stringify(t0));
console.log('after12s:',JSON.stringify(t1));
console.log('afterStop:',JSON.stringify(t2));
console.log('ERRORS',errs.length,errs.join(';'));
await b.close();
